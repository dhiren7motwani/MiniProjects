def pri(a,b,c,d):
  name=[1,2,3,4]
  values=[a,b,c,d]
  values,name=(list(t) for t in zip(*sorted(zip(values,name))))
  values=[2,4,6,8]
  name,values=(list(t) for t in zip(*sorted(zip(name,values))))
  return values
 
